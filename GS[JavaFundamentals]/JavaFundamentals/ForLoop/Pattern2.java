package ForLoop;

public class Pattern2 {

	public static void main(String[] args) {

		for(int i=1 ; i<=5; i++)
		{
			for(int j=1 ; j<=i ; j++)
			{
				System.out.print(j);
				//print even/odd/floyd's triangle(increasing)
			}
			//print 1 in first line | print 2 2 in second line and so on..
			//num = i+1
			System.out.println();
		}

	}

}
